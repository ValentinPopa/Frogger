public class Lane  
{
    //declarari
    private int x;
    private int y;
    //constructor
    public Lane(int x, int y)
    {
        this.x = x;
        this.y = y;
    }
    //returnare x privat
    public int getX() {
     return x;   
    }
    //returnare y privat
    public int getY() {
        return y;
    }
}
